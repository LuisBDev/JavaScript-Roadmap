let result;
result = Math.PI;
console.log(result);

//Redondeando
result = Math.round(2.6);
console.log(result);

//Redondear hacia arriba
result = Math.ceil(2, 1);
console.log(result);

//Redondear hacia abajo
result = Math.floor(2, 1);
console.log(result);

//sqr
result = Math.sqrt(64);
console.log(result);

//absoluto
result = Math.abs(-233);
console.log(result);

//potencia
result = Math.pow(2, 3);
console.log(result);

//min valor
result = Math.min(-123, 3, 5, 6, 7, 111);
console.log(result);

//max valor
result = Math.max(1, 2, 6, 123, 555);
console.log(result);

//random
result = Math.random();
console.log(result);

